public class vetores {
    public static void main(String[] args) {
        int[] numeros = new int[5];
        numeros[0] = 10;
        numeros[1] = 20;
        numeros[2] = 30;
        numeros[3] = 40;
        numeros[4] = 50;

        int i=0;

        while(i <= 5){
            System.out.println(numeros[i]);
            i = i + 1;
        }
    }
}